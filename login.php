        <div class="login-card">
            <div class="logo">
                <i class="fas fa-calendar-alt"></i>
                <h1>Shift Scheduler</h1>
            </div>
            
            <?php if (isset($error)): ?>
                <div class="alert alert-danger"><?php echo $error; ?></div>
            <?php endif; ?>
            
            <form method="POST">
                <div class="form-group">
